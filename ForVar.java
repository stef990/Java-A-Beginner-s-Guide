//Declare loop control variable inside the for.
class ForVar{
public static void main(String args[]){
	int sum = 0;
	int fact = 1;
	
	//compute factorial of numbers through 5
	for(int i =1;i<=5;i++){
		sum += i;
		fact *= i;
		};
		
		System.out.println("Sum is " + sum);
		System.out.println("Factorial is " + fact);
	}}
	